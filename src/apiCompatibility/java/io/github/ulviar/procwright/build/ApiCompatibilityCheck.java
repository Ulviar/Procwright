    private static void assertSameMethodDescriptor(Method before, Method after, String fixtureDescription) {
        MethodType beforeDescriptor = MethodType.methodType(before.getReturnType(), before.getParameterTypes());
        MethodType afterDescriptor = MethodType.methodType(after.getReturnType(), after.getParameterTypes());
        if (!beforeDescriptor.equals(afterDescriptor)) {
            throw new AssertionError(fixtureDescription + " must have the same JVM method descriptor");
        }
    }

    private static void assertSameConstructorDescriptor(
            Constructor<?> before, Constructor<?> after, String fixtureDescription) {
        MethodType beforeDescriptor = MethodType.methodType(void.class, before.getParameterTypes());
        MethodType afterDescriptor = MethodType.methodType(void.class, after.getParameterTypes());
        if (!beforeDescriptor.equals(afterDescriptor)) {
            throw new AssertionError(fixtureDescription + " must have the same JVM constructor descriptor");
        }
    }

    private static void assertSignature(String expected, String actual, String fixtureDescription) {
        if (!expected.equals(actual)) {
            throw new AssertionError(
                    fixtureDescription + " signature mismatch: expected `" + expected + "`, got `" + actual + '`');
        }
    }

    private static String fixtureSignature(Method method) {
        return methodSignature(method).replace(method.getDeclaringClass().getName(), "fixture");
    }

    private static String fixtureSignature(Constructor<?> constructor) {
        return constructorSignature(constructor)
                .replace(constructor.getDeclaringClass().getName(), "fixture");
    }

    private static void assertBaselineRejectsChangedSignature(String before, String after, String change)
            throws IOException {
        assertBaselineRejectsChangedSignatures(Set.of(before), Set.of(after), change, true, true);
    }

    private static void assertBaselineRejectsChangedSignatures(
            Set<String> before, Set<String> after, String change, boolean expectRemovedOrChanged, boolean expectAdded)
            throws IOException {
        Path baseline = Files.createTempFile("procwright-api-" + change.replace(' ', '-') + "-", ".txt");
        try {
            Files.write(baseline, before, StandardCharsets.UTF_8);
            ArtifactSpec fixture = new ArtifactSpec("self-test-fixture", baseline, List.of(), List.of(), Set.of());
            try {
                verifyBaseline(fixture, new TreeSet<>(after));
                throw new AssertionError("exact API verification accepted a changed " + change + " signature");
            } catch (IllegalStateException expected) {
                boolean reportedRemovedOrChanged = expected.getMessage().contains("Removed or changed signatures");
                boolean reportedAdded = expected.getMessage().contains("Added signatures");
                if (reportedRemovedOrChanged != expectRemovedOrChanged || reportedAdded != expectAdded) {
                    throw new AssertionError("exact API verification reported an unexpected diff", expected);
                }
            }
        } finally {
            Files.deleteIfExists(baseline);
        }
    }

