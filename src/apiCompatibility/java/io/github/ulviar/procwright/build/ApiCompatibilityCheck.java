    private record ArtifactSpec(
            String name, Path baseline, List<Path> roots, List<Path> classpath, Set<String> packages) {

        static ArtifactSpec parse(String spec) {
            String[] parts = spec.split("\\|", -1);
            if (parts.length != 5) {
                throw new IllegalArgumentException("Invalid artifact spec: " + spec);
            }
            return new ArtifactSpec(
                    requireText(parts[0], "name"),
                    Path.of(requireText(parts[1], "baseline")),
                    paths(parts[2]),
                    paths(parts[3]),
                    Arrays.stream(requireText(parts[4], "packages").split(","))
                            .map(String::trim)
                            .filter(value -> !value.isEmpty())
                            .collect(Collectors.toUnmodifiableSet()));
        }

        private static List<Path> paths(String value) {
            if (value.isBlank()) {
                return List.of();
            }
            return Arrays.stream(value.split(java.util.regex.Pattern.quote(File.pathSeparator)))
                    .filter(path -> !path.isBlank())
                    .map(Path::of)
                    .toList();
        }

        private static String requireText(String value, String name) {
            return Objects.requireNonNull(value, name).isBlank() ? throwBlank(name) : value;
        }

        private static String throwBlank(String name) {
            throw new IllegalArgumentException(name + " must not be blank");
        }
    }
