package io.github.ulviar.procwright.build;

import java.io.File;
import java.io.IOException;
import java.lang.reflect.Constructor;
import java.lang.reflect.Field;
import java.lang.reflect.GenericArrayType;
import java.lang.reflect.Method;
import java.lang.reflect.Modifier;
import java.lang.reflect.ParameterizedType;
import java.lang.reflect.Type;
import java.lang.reflect.TypeVariable;
import java.lang.reflect.WildcardType;
import java.net.URL;
import java.net.URLClassLoader;
import java.nio.charset.StandardCharsets;
import java.nio.file.Files;
import java.nio.file.Path;
import java.util.ArrayList;
import java.util.Arrays;
import java.util.Collection;
import java.util.List;
import java.util.Objects;
import java.util.Set;
import java.util.TreeSet;
import java.util.jar.JarFile;
import java.util.stream.Collectors;

public final class ApiCompatibilityCheck {

    private static final String BASELINE_HEADER =
            "# Generated by ./gradlew writeApiCompatibilityBaseline --project-prop=procwright.javaRelease=17";

    private ApiCompatibilityCheck() {}

    public static void main(String[] args) throws Exception {
        if (args.length < 2) {
            throw new IllegalArgumentException("Usage: ApiCompatibilityCheck <check|write> <artifact-spec>...");
        }
        Mode mode =
                switch (args[0]) {
                    case "check" -> Mode.CHECK;
                    case "write" -> Mode.WRITE;
                    default -> throw new IllegalArgumentException("Unknown mode: " + args[0]);
                };

        for (int index = 1; index < args.length; index++) {
            ArtifactSpec spec = ArtifactSpec.parse(args[index]);
            TreeSet<String> current = snapshot(spec);
            if (mode == Mode.WRITE) {
                writeBaseline(spec, current);
            } else {
                verifyBaseline(spec, current);
            }
        }
    }

    private static TreeSet<String> snapshot(ArtifactSpec spec) throws Exception {
        TreeSet<String> classNames = new TreeSet<>();
        for (Path root : spec.roots()) {
            collectClassNames(root, classNames);
        }

        TreeSet<String> signatures = new TreeSet<>();
        try (URLClassLoader classLoader = new URLClassLoader(urls(spec), ClassLoader.getPlatformClassLoader())) {
            for (String className : classNames) {
                Class<?> type = Class.forName(className, false, classLoader);
                if (isPublicApiType(type, spec.packages())) {
                    signatures.addAll(typeSignatures(type));
                }
            }
        }
        return signatures;
    }

    private static void writeBaseline(ArtifactSpec spec, TreeSet<String> signatures) throws IOException {
        Files.createDirectories(spec.baseline().getParent());
        ArrayList<String> lines = new ArrayList<>();
        lines.add("# Procwright public JVM API baseline for " + spec.name());
        lines.add(BASELINE_HEADER);
        lines.add("");
        lines.addAll(signatures);
        Files.write(spec.baseline(), lines, StandardCharsets.UTF_8);
    }

    private static void verifyBaseline(ArtifactSpec spec, TreeSet<String> current) throws IOException {
        if (!Files.isRegularFile(spec.baseline())) {
            throw new IllegalStateException("API baseline is missing for " + spec.name() + ": " + spec.baseline());
        }
        TreeSet<String> baseline = Files.readAllLines(spec.baseline(), StandardCharsets.UTF_8).stream()
                .map(String::trim)
                .filter(line -> !line.isEmpty())
                .filter(line -> !line.startsWith("#"))
                .collect(Collectors.toCollection(TreeSet::new));

        TreeSet<String> removedOrChanged = new TreeSet<>(baseline);
        removedOrChanged.removeAll(current);

        TreeSet<String> added = new TreeSet<>(current);
        added.removeAll(baseline);

        if (!removedOrChanged.isEmpty() || !added.isEmpty()) {
            StringBuilder message = new StringBuilder();
            message.append("Public API for ").append(spec.name()).append(" no longer matches the 0.1.0 baseline.");
            appendDiff(message, "Removed or changed signatures", removedOrChanged);
            appendDiff(message, "Added signatures", added);
            message.append(System.lineSeparator())
                    .append("If this is intentional, update the API baseline together with the API decision.");
            throw new IllegalStateException(message.toString());
        }
    }

    private static void appendDiff(StringBuilder message, String title, Set<String> lines) {
        if (lines.isEmpty()) {
            return;
        }
        message.append(System.lineSeparator()).append(title).append(":");
        lines.stream().limit(80).forEach(line -> message.append(System.lineSeparator())
                .append("  ")
                .append(line));
        if (lines.size() > 80) {
            message.append(System.lineSeparator())
                    .append("  ... ")
                    .append(lines.size() - 80)
                    .append(" more");
        }
    }

    private static URL[] urls(ArtifactSpec spec) throws IOException {
        TreeSet<Path> paths = new TreeSet<>();
        paths.addAll(spec.roots());
        paths.addAll(spec.classpath());
        ArrayList<URL> urls = new ArrayList<>();
        for (Path path : paths) {
            urls.add(path.toUri().toURL());
        }
        return urls.toArray(URL[]::new);
    }

    private static void collectClassNames(Path root, Set<String> classNames) throws IOException {
        if (Files.isDirectory(root)) {
            try (var paths = Files.walk(root)) {
                for (Path classFile : paths.filter(Files::isRegularFile)
                        .filter(path -> path.getFileName().toString().endsWith(".class"))
                        .toList()) {
                    addClassName(root, classFile, classNames);
                }
            }
            return;
        }

        try (JarFile jar = new JarFile(root.toFile())) {
            jar.stream()
                    .filter(entry -> !entry.isDirectory())
                    .filter(entry -> entry.getName().endsWith(".class"))
                    .forEach(entry -> addClassName(entry.getName(), classNames));
        }
    }

    private static void addClassName(Path root, Path classFile, Set<String> classNames) {
        String relative = root.relativize(classFile).toString().replace(File.separatorChar, '/');
        addClassName(relative, classNames);
    }

    private static void addClassName(String relativeClassFile, Set<String> classNames) {
        String fileName = relativeClassFile.substring(relativeClassFile.lastIndexOf('/') + 1);
        if ("module-info.class".equals(fileName) || "package-info.class".equals(fileName)) {
            return;
        }
        classNames.add(relativeClassFile
                .substring(0, relativeClassFile.length() - ".class".length())
                .replace('/', '.'));
    }

    private static boolean isPublicApiType(Class<?> type, Set<String> packages) {
        return Modifier.isPublic(type.getModifiers())
                && packages.contains(type.getPackageName())
                && !type.isSynthetic();
    }

    private static List<String> typeSignatures(Class<?> type) {
        TreeSet<String> signatures = new TreeSet<>();
        signatures.add(typeSignature(type));

        for (Constructor<?> constructor : type.getDeclaredConstructors()) {
            if (isPublicOrProtected(constructor.getModifiers()) && !constructor.isSynthetic()) {
                signatures.add(constructorSignature(constructor));
            }
        }
        for (Method method : type.getDeclaredMethods()) {
            if (isPublicOrProtected(method.getModifiers()) && !method.isSynthetic() && !method.isBridge()) {
                signatures.add(methodSignature(method));
            }
        }
        for (Field field : type.getDeclaredFields()) {
            if (isPublicOrProtected(field.getModifiers()) && !field.isSynthetic()) {
                signatures.add(fieldSignature(field));
            }
        }
        return List.copyOf(signatures);
    }

    private static boolean isPublicOrProtected(int modifiers) {
        return Modifier.isPublic(modifiers) || Modifier.isProtected(modifiers);
    }

    private static String typeSignature(Class<?> type) {
        StringBuilder signature = new StringBuilder();
        signature
                .append("TYPE ")
                .append(renderModifiers(type.getModifiers()))
                .append(' ')
                .append(typeKind(type))
                .append(' ')
                .append(type.getName())
                .append(renderTypeParameters(type.getTypeParameters()));

        Type superclass = type.getGenericSuperclass();
        if (superclass != null && superclass != Object.class) {
            signature.append(" extends ").append(renderType(superclass));
        }

        Type[] interfaces = type.getGenericInterfaces();
        if (interfaces.length > 0) {
            signature
                    .append(type.isInterface() ? " extends " : " implements ")
                    .append(renderTypes(List.of(interfaces)));
        }

        Class<?>[] permittedSubclasses = type.getPermittedSubclasses();
        if (permittedSubclasses != null && permittedSubclasses.length > 0) {
            signature
                    .append(" permits ")
                    .append(Arrays.stream(permittedSubclasses)
                            .map(Class::getName)
                            .sorted()
                            .collect(Collectors.joining(", ")));
        }
        return normalize(signature.toString());
    }

    private static String constructorSignature(Constructor<?> constructor) {
        return normalize("CTOR "
                + renderModifiers(constructor.getModifiers())
                + ' '
                + renderTypeParameters(constructor.getTypeParameters())
                + (constructor.getTypeParameters().length == 0 ? "" : " ")
                + constructor.getDeclaringClass().getName()
                + '('
                + renderExecutableParameters(constructor.getGenericParameterTypes(), constructor.isVarArgs())
                + ')');
    }

    private static String methodSignature(Method method) {
        return normalize("METHOD "
                + renderModifiers(method.getModifiers())
                + ' '
                + renderTypeParameters(method.getTypeParameters())
                + (method.getTypeParameters().length == 0 ? "" : " ")
                + renderType(method.getGenericReturnType())
                + ' '
                + method.getDeclaringClass().getName()
                + '#'
                + method.getName()
                + '('
                + renderExecutableParameters(method.getGenericParameterTypes(), method.isVarArgs())
                + ')');
    }

    private static String fieldSignature(Field field) {
        return normalize("FIELD "
                + renderModifiers(field.getModifiers())
                + ' '
                + renderType(field.getGenericType())
                + ' '
                + field.getDeclaringClass().getName()
                + '#'
                + field.getName());
    }

    private static String typeKind(Class<?> type) {
        if (type.isAnnotation()) {
            return "@interface";
        }
        if (type.isEnum()) {
            return "enum";
        }
        if (type.isRecord()) {
            return "record";
        }
        if (type.isInterface()) {
            return "interface";
        }
        return "class";
    }

    private static String renderExecutableParameters(Type[] parameters, boolean varArgs) {
        ArrayList<String> rendered = new ArrayList<>();
        for (int index = 0; index < parameters.length; index++) {
            String parameter = renderType(parameters[index]);
            if (varArgs && index == parameters.length - 1 && parameter.endsWith("[]")) {
                parameter = parameter.substring(0, parameter.length() - 2) + "...";
            }
            rendered.add(parameter);
        }
        return String.join(", ", rendered);
    }

    private static String renderTypeParameters(TypeVariable<?>[] parameters) {
        if (parameters.length == 0) {
            return "";
        }
        return Arrays.stream(parameters)
                .map(ApiCompatibilityCheck::renderTypeParameter)
                .collect(Collectors.joining(", ", "<", ">"));
    }

    private static String renderTypeParameter(TypeVariable<?> parameter) {
        List<String> bounds = Arrays.stream(parameter.getBounds())
                .filter(bound -> bound != Object.class)
                .map(ApiCompatibilityCheck::renderType)
                .toList();
        if (bounds.isEmpty()) {
            return parameter.getName();
        }
        return parameter.getName() + " extends " + String.join(" & ", bounds);
    }

    private static String renderTypes(Collection<Type> types) {
        return types.stream().map(ApiCompatibilityCheck::renderType).collect(Collectors.joining(", "));
    }

    private static String renderType(Type type) {
        if (type instanceof Class<?> clazz) {
            if (clazz.isArray()) {
                return renderType(clazz.componentType()) + "[]";
            }
            return clazz.getName();
        }
        if (type instanceof ParameterizedType parameterizedType) {
            return renderType(parameterizedType.getRawType())
                    + Arrays.stream(parameterizedType.getActualTypeArguments())
                            .map(ApiCompatibilityCheck::renderType)
                            .collect(Collectors.joining(", ", "<", ">"));
        }
        if (type instanceof TypeVariable<?> typeVariable) {
            return typeVariable.getName();
        }
        if (type instanceof WildcardType wildcardType) {
            Type[] lowerBounds = wildcardType.getLowerBounds();
            if (lowerBounds.length > 0) {
                return "? super " + renderTypes(List.of(lowerBounds));
            }
            Type[] upperBounds = wildcardType.getUpperBounds();
            if (upperBounds.length == 0 || (upperBounds.length == 1 && upperBounds[0] == Object.class)) {
                return "?";
            }
            return "? extends " + renderTypes(List.of(upperBounds));
        }
        if (type instanceof GenericArrayType arrayType) {
            return renderType(arrayType.getGenericComponentType()) + "[]";
        }
        return type.getTypeName();
    }

    private static String renderModifiers(int modifiers) {
        int publicApiModifiers = modifiers
                & (Modifier.PUBLIC
                        | Modifier.PROTECTED
                        | Modifier.STATIC
                        | Modifier.FINAL
                        | Modifier.ABSTRACT
                        | Modifier.SYNCHRONIZED
                        | Modifier.NATIVE
                        | Modifier.STRICT);
        return Modifier.toString(publicApiModifiers);
    }

    private static String normalize(String signature) {
        return signature.replaceAll("\\s+", " ").trim();
    }

    private enum Mode {
        CHECK,
        WRITE
    }

    private record ArtifactSpec(
            String name, Path baseline, List<Path> roots, List<Path> classpath, Set<String> packages) {

        static ArtifactSpec parse(String spec) {
            String[] parts = spec.split("\\|", -1);
            if (parts.length != 5) {
                throw new IllegalArgumentException("Invalid artifact spec: " + spec);
            }
            return new ArtifactSpec(
                    requireText(parts[0], "name"),
                    Path.of(requireText(parts[1], "baseline")),
                    paths(parts[2]),
                    paths(parts[3]),
                    Arrays.stream(requireText(parts[4], "packages").split(","))
                            .map(String::trim)
                            .filter(value -> !value.isEmpty())
                            .collect(Collectors.toUnmodifiableSet()));
        }

        private static List<Path> paths(String value) {
            if (value.isBlank()) {
                return List.of();
            }
            return Arrays.stream(value.split(java.util.regex.Pattern.quote(File.pathSeparator)))
                    .filter(path -> !path.isBlank())
                    .map(Path::of)
                    .toList();
        }

        private static String requireText(String value, String name) {
            return Objects.requireNonNull(value, name).isBlank() ? throwBlank(name) : value;
        }

        private static String throwBlank(String name) {
            throw new IllegalArgumentException(name + " must not be blank");
        }
    }
}
