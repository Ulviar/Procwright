    private fun verifySyntheticTypeBranches() {
        // Kotlin source cannot explicitly spell a flexible platform type. Constructing the
        // supported metadata node makes its lower/upper nullness traversal deterministic.
        val typeParameter = KmTypeParameter("T", 7, KmVariance.INVARIANT)
        val definitelyNonNull =
            kmType(KmClassifier.TypeParameter(typeParameter.id)).apply {
                isDefinitelyNonNull = true
            }
        val root =
            kmType(KmClassifier.Class("fixture/Inner")).apply {
                arguments += KmTypeProjection.STAR
                arguments += KmTypeProjection(KmVariance.INVARIANT, definitelyNonNull)
                outerType = nullableType(KmClassifier.Class("fixture/Outer"))
                abbreviatedType = kmType(KmClassifier.TypeAlias("fixture/Alias"))
                flexibleTypeUpperBound =
                    KmFlexibleTypeUpperBound(
                        nullableType(KmClassifier.Class("kotlin/Any")),
                        "kotlin.jvm.PlatformType",
                    )
            }
        val declaration = "FUNCTION fixture.Branches#types()Ljava/lang/Object;"
        val actual = buildSet { addType(declaration, "return", root, mapOf(7 to typeParameter)) }
        val expected =
            setOf(
                "KOTLIN_TYPE\t$declaration\treturn\tNON_NULL\tCLASS:fixture.Inner",
                "KOTLIN_PROJECTION\t$declaration\treturn.typeArgument[0]\tSTAR",
                "KOTLIN_PROJECTION\t$declaration\treturn.typeArgument[1]\tINVARIANT",
                "KOTLIN_TYPE\t$declaration\treturn.typeArgument[1]\tDEFINITELY_NON_NULL\tTYPE_PARAMETER:T",
                "KOTLIN_TYPE\t$declaration\treturn.owner\tNULLABLE\tCLASS:fixture.Outer",
                "KOTLIN_TYPE\t$declaration\treturn.abbreviated\tNON_NULL\tTYPE_ALIAS:fixture.Alias",
                "KOTLIN_FLEXIBILITY\t$declaration\treturn\tkotlin.jvm.PlatformType",
                "KOTLIN_TYPE\t$declaration\treturn.flexibleUpperBound\tNULLABLE\tCLASS:kotlin.Any",
            )

        check(actual == expected) {
            "Synthetic type branches changed: expected $expected, got $actual"
        }
    }

    private fun verifyPropertyAndTypeAliasTraversalMutations() {
        val propertyOwner = "fixture.PropertyApi"
        val approvedProperty = buildSet {
            addProperty(propertyOwner, propertyTraversalFixture(), emptyMap())
        }
        assertOnlyNullabilityMutation(
            approvedProperty,
            buildSet {
                addProperty(
                    propertyOwner,
                    propertyTraversalFixture(nullableBound = true),
                    emptyMap(),
                )
            },
            "typeParameter[T].bound[0]",
        )
        assertOnlyNullabilityMutation(
            approvedProperty,
            buildSet {
                addProperty(
                    propertyOwner,
                    propertyTraversalFixture(nullableReceiver = true),
                    emptyMap(),
                )
            },
            "receiver",
        )

        val aliasOwner = "fixture.AliasApi"
        val approvedAlias = buildSet {
            addTypeAlias(aliasOwner, typeAliasTraversalFixture(), emptyMap())
        }
        assertOnlyNullabilityMutation(
            approvedAlias,
            buildSet {
                addTypeAlias(
                    aliasOwner,
                    typeAliasTraversalFixture(nullableUnderlying = true),
                    emptyMap(),
                )
            },
            "underlying",
        )
        assertOnlyNullabilityMutation(
            approvedAlias,
            buildSet {
                addTypeAlias(
                    aliasOwner,
                    typeAliasTraversalFixture(nullableExpanded = true),
                    emptyMap(),
                )
            },
            "expanded",
        )
    }

    private fun propertyTraversalFixture(
        nullableBound: Boolean = false,
        nullableReceiver: Boolean = false,
    ): KmProperty {
        val typeParameter = KmTypeParameter("T", 31, KmVariance.INVARIANT)
        typeParameter.upperBounds +=
            if (nullableBound) nullableType(KmClassifier.Class("kotlin/String"))
            else kmType(KmClassifier.Class("kotlin/String"))
        val typeParameterType = kmType(KmClassifier.TypeParameter(typeParameter.id))
        val receiver =
            kmType(KmClassifier.Class("kotlin/collections/List")).apply {
                arguments += KmTypeProjection(KmVariance.INVARIANT, typeParameterType)
                isNullable = nullableReceiver
            }
        return KmProperty("mapped").apply {
            visibility = Visibility.PUBLIC
            getterSignature =
                JvmMethodSignature("getMapped", "(Ljava/util/List;)Ljava/lang/Object;")
            typeParameters += typeParameter
            receiverParameterType = receiver
            returnType = typeParameterType
        }
    }

    private fun typeAliasTraversalFixture(
