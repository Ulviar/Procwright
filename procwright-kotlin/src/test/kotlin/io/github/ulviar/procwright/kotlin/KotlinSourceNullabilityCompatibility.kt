        nullableExpanded: Boolean = false,
    ): KmTypeAlias =
        KmTypeAlias("Alias").apply {
            visibility = Visibility.PUBLIC
            underlyingType =
                kmType(KmClassifier.Class("kotlin/collections/List")).apply {
                    isNullable = nullableUnderlying
                }
            expandedType =
                kmType(KmClassifier.Class("kotlin/collections/Collection")).apply {
