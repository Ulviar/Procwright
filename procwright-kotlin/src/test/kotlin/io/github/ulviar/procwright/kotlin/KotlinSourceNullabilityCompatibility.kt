    private fun assertOnlyNullabilityMutation(
        approved: Set<String>,
        hostile: Set<String>,
        expectedPosition: String,
    ) {
        val diff = compatibilityDiff(approved, hostile)
        check(
            diff.removedOrChanged.size == 1 &&
                position(diff.removedOrChanged.single()) == expectedPosition &&
                diff.removedOrChanged.single().contains("\tNON_NULL\t")
        ) {
            "Approved metadata did not expose exactly '$expectedPosition': $diff"
        }
        check(
            diff.added.size == 1 &&
                position(diff.added.single()) == expectedPosition &&
                diff.added.single().contains("\tNULLABLE\t")
        ) {
            "Hostile metadata did not mutate exactly '$expectedPosition': $diff"
        }
    }

    private fun verifySyntheticContextAndAccessorBranches() {
        // Current Kotlin source cannot emit legacy context receivers and named context
        // parameters on one declaration, while the supported metadata model exposes both.
        val stringType = kmType(KmClassifier.Class("kotlin/String"))
        val function =
            KmFunction("contextual").apply {
                visibility = Visibility.PUBLIC
                signature =
                    JvmMethodSignature("contextual", "(Ljava/lang/String;)Ljava/lang/String;")
                contextReceiverTypes += stringType
                contextParameters += valueParameter("scope", stringType)
                valueParameters += valueParameter("input", stringType)
                returnType = stringType
            }
        val property =
            KmProperty("value").apply {
                visibility = Visibility.PUBLIC
                getterSignature = JvmMethodSignature("getValue", "()Ljava/lang/String;")
                contextReceiverTypes += stringType
                contextParameters += valueParameter("propertyScope", stringType)
                returnType = stringType
                setter = KmPropertyAccessorAttributes().apply { visibility = Visibility.PUBLIC }
                setterParameter = valueParameter("incoming", stringType)
            }
        val actual = buildSet {
            addFunction("fixture.ContextApi", function, emptyMap())
            addProperty("fixture.ContextApi", property, emptyMap())
        }
        val functionDeclaration =
            "FUNCTION fixture.ContextApi#contextual(Ljava/lang/String;)Ljava/lang/String;"
        val propertyDeclaration = "PROPERTY fixture.ContextApi#getValue()Ljava/lang/String;"
        val expected =
            setOf(
                "KOTLIN_TYPE\t$functionDeclaration\tcontextReceiver[0]\tNON_NULL\tCLASS:kotlin.String",
                "KOTLIN_PARAMETER_NAME\t$functionDeclaration\tcontextParameter[0]\tscope",
                "KOTLIN_TYPE\t$functionDeclaration\tcontextParameter[0]\tNON_NULL\tCLASS:kotlin.String",
                "KOTLIN_PARAMETER_NAME\t$functionDeclaration\tparameter[0]\tinput",
                "KOTLIN_TYPE\t$functionDeclaration\tparameter[0]\tNON_NULL\tCLASS:kotlin.String",
                "KOTLIN_TYPE\t$functionDeclaration\treturn\tNON_NULL\tCLASS:kotlin.String",
                "KOTLIN_TYPE\t$propertyDeclaration\tcontextReceiver[0]\tNON_NULL\tCLASS:kotlin.String",
                "KOTLIN_PARAMETER_NAME\t$propertyDeclaration\tcontextParameter[0]\tpropertyScope",
                "KOTLIN_TYPE\t$propertyDeclaration\tcontextParameter[0]\tNON_NULL\tCLASS:kotlin.String",
                "KOTLIN_TYPE\t$propertyDeclaration\treturn\tNON_NULL\tCLASS:kotlin.String",
                "KOTLIN_PARAMETER_NAME\t$propertyDeclaration\tsetterParameter\tincoming",
                "KOTLIN_TYPE\t$propertyDeclaration\tsetterParameter\tNON_NULL\tCLASS:kotlin.String",
            )

        check(actual == expected) {
            "Synthetic context/accessor branches changed: expected $expected, got $actual"
        }
    }

    private fun verifyMultifileMetadataBranches() {
        val facade = fixtureClasses("approved", "MultifileApi").single()
        val firstPart = fixtureClasses("approved", "MultifileApi__MultifileFirstKt").single()
        val secondPart = fixtureClasses("approved", "MultifileApi__MultifileSecondKt").single()
        check(
            KotlinClassMetadata.readStrict(facade.getAnnotation(Metadata::class.java))
                is KotlinClassMetadata.MultiFileClassFacade
        ) {
            "Compiled multifile facade did not produce MultiFileClassFacade metadata"
        }
        check(
            KotlinClassMetadata.readStrict(firstPart.getAnnotation(Metadata::class.java))
                is KotlinClassMetadata.MultiFileClassPart
        ) {
            "Compiled multifile part did not produce MultiFileClassPart metadata"
        }

        val snapshot = normalizedFixtureSnapshot(listOf(facade, firstPart, secondPart), "approved")
        assertContains(snapshot, "FUNCTION fixture.MultifileApi__MultifileFirstKt#multifileFirst")
        assertContains(snapshot, "FUNCTION fixture.MultifileApi__MultifileSecondKt#multifileSecond")
    }

    private fun snapshot(classes: Collection<Class<*>>): Set<String> = buildSet {
        classes.sortedBy(Class<*>::getName).forEach { type ->
            val metadata = KotlinClassMetadata.readStrict(type.getAnnotation(Metadata::class.java))
            when (metadata) {
                is KotlinClassMetadata.Class ->
                    if (isEffectivelyPublicKotlinClass(type)) {
                        addClass(type.name, metadata.kmClass)
                    }
                is KotlinClassMetadata.FileFacade -> addPackage(type.name, metadata.kmPackage)
                is KotlinClassMetadata.MultiFileClassPart ->
                    addPackage(type.name, metadata.kmPackage)
                // These metadata kinds have no declaration container, so no supported public
                // receiver, parameter, return, bound, property, constructor, or alias is hidden.
                is KotlinClassMetadata.MultiFileClassFacade,
                is KotlinClassMetadata.SyntheticClass,
                is KotlinClassMetadata.Unknown -> Unit
            }
        }
    }

    private fun MutableSet<String>.addClass(owner: String, declaration: KmClass) {
        val classTypeParameters = declaration.typeParameters.associateBy(KmTypeParameter::id)
        addTypeParameters("CLASS $owner", declaration.typeParameters, classTypeParameters)
        declaration.supertypes.forEachIndexed { index, type ->
            addType("CLASS $owner", "supertype[$index]", type, classTypeParameters)
        }
        addContainer(owner, declaration, classTypeParameters)
        declaration.constructors
            .filter { it.visibility.isApiVisibility() }
            .forEach { constructor -> addConstructor(owner, constructor, classTypeParameters) }
    }

    private fun MutableSet<String>.addPackage(owner: String, declaration: KmPackage) {
        addContainer(owner, declaration, emptyMap())
    }

    private fun MutableSet<String>.addContainer(
        owner: String,
        declaration: KmDeclarationContainer,
        parentTypeParameters: Map<Int, KmTypeParameter>,
    ) {
        declaration.functions
            .filter { it.visibility.isApiVisibility() }
            .forEach { function -> addFunction(owner, function, parentTypeParameters) }
        declaration.properties
            .filter { it.visibility.isApiVisibility() }
            .forEach { property -> addProperty(owner, property, parentTypeParameters) }
        declaration.typeAliases
            .filter { it.visibility.isApiVisibility() }
            .forEach { alias -> addTypeAlias(owner, alias, parentTypeParameters) }
    }

    private fun MutableSet<String>.addFunction(
        owner: String,
        function: KmFunction,
        parentTypeParameters: Map<Int, KmTypeParameter>,
    ) {
        val signature =
            checkNotNull(function.signature) {
                "Public Kotlin function has no JVM signature: $owner#${function.name}"
            }
        val declaration = "FUNCTION $owner#${signature.name}${signature.descriptor}"
        val typeParameters =
            parentTypeParameters + function.typeParameters.associateBy(KmTypeParameter::id)
        addTypeParameters(declaration, function.typeParameters, typeParameters)
        function.receiverParameterType?.let { addType(declaration, "receiver", it, typeParameters) }
        function.contextReceiverTypes.forEachIndexed { index, type ->
            addType(declaration, "contextReceiver[$index]", type, typeParameters)
        }
        function.contextParameters.forEachIndexed { index, parameter ->
            addValueParameter(declaration, "contextParameter[$index]", parameter, typeParameters)
        }
        function.valueParameters.forEachIndexed { index, parameter ->
            addValueParameter(declaration, "parameter[$index]", parameter, typeParameters)
        }
        addType(declaration, "return", function.returnType, typeParameters)
    }

    private fun MutableSet<String>.addConstructor(
        owner: String,
        constructor: KmConstructor,
        typeParameters: Map<Int, KmTypeParameter>,
    ) {
        val signature =
            checkNotNull(constructor.signature) {
                "Public Kotlin constructor has no JVM signature: $owner"
            }
        val declaration = "CONSTRUCTOR $owner#${signature.name}${signature.descriptor}"
        constructor.valueParameters.forEachIndexed { index, parameter ->
            addValueParameter(declaration, "parameter[$index]", parameter, typeParameters)
        }
    }

    private fun MutableSet<String>.addProperty(
        owner: String,
        property: KmProperty,
        parentTypeParameters: Map<Int, KmTypeParameter>,
    ) {
        val signature = property.getterSignature
        val declaration =
            "PROPERTY $owner#${signature?.name ?: property.name}${signature?.descriptor ?: ""}"
        val typeParameters =
            parentTypeParameters + property.typeParameters.associateBy(KmTypeParameter::id)
        addTypeParameters(declaration, property.typeParameters, typeParameters)
        property.receiverParameterType?.let { addType(declaration, "receiver", it, typeParameters) }
        property.contextReceiverTypes.forEachIndexed { index, type ->
            addType(declaration, "contextReceiver[$index]", type, typeParameters)
        }
        property.contextParameters.forEachIndexed { index, parameter ->
            addValueParameter(declaration, "contextParameter[$index]", parameter, typeParameters)
        }
        addType(declaration, "return", property.returnType, typeParameters)
        if (property.setter?.visibility?.isApiVisibility() == true) {
            property.setterParameter?.let {
                addValueParameter(declaration, "setterParameter", it, typeParameters)
            }
        }
    }

    private fun MutableSet<String>.addValueParameter(
        declaration: String,
        position: String,
        parameter: KmValueParameter,
        typeParameters: Map<Int, KmTypeParameter>,
    ) {
        add("KOTLIN_PARAMETER_NAME\t$declaration\t$position\t${parameter.name}")
        addType(declaration, position, parameter.type, typeParameters)
        parameter.varargElementType?.let {
            addType(declaration, "$position.varargElement", it, typeParameters)
        }
    }

    private fun MutableSet<String>.addTypeAlias(
        owner: String,
        alias: KmTypeAlias,
        parentTypeParameters: Map<Int, KmTypeParameter>,
    ) {
        val declaration = "TYPE_ALIAS $owner#${alias.name}"
        val typeParameters =
            parentTypeParameters + alias.typeParameters.associateBy(KmTypeParameter::id)
        addTypeParameters(declaration, alias.typeParameters, typeParameters)
        addType(declaration, "underlying", alias.underlyingType, typeParameters)
        addType(declaration, "expanded", alias.expandedType, typeParameters)
    }

    private fun MutableSet<String>.addTypeParameters(
        declaration: String,
        parameters: List<KmTypeParameter>,
        typeParameters: Map<Int, KmTypeParameter>,
    ) {
        parameters.forEach { parameter ->
            parameter.upperBounds.forEachIndexed { index, bound ->
                addType(
                    declaration,
                    "typeParameter[${parameter.name}].bound[$index]",
                    bound,
                    typeParameters,
                )
            }
        }
    }

    private fun MutableSet<String>.addType(
        declaration: String,
        position: String,
        type: KmType,
        typeParameters: Map<Int, KmTypeParameter>,
    ) {
        add(
            listOf(
                    "KOTLIN_TYPE",
                    declaration,
                    position,
                    type.nullability(),
                    type.classifier.render(typeParameters),
                )
                .joinToString("\t")
        )
        type.arguments.forEachIndexed { index, projection ->
            val argumentPosition = "$position.typeArgument[$index]"
            val argumentType = projection.type
            if (argumentType == null) {
                add("KOTLIN_PROJECTION\t$declaration\t$argumentPosition\tSTAR")
            } else {
                add("KOTLIN_PROJECTION\t$declaration\t$argumentPosition\t${projection.variance}")
                addType(declaration, argumentPosition, argumentType, typeParameters)
            }
        }
        type.outerType?.let { addType(declaration, "$position.owner", it, typeParameters) }
        type.abbreviatedType?.let {
            addType(declaration, "$position.abbreviated", it, typeParameters)
        }
        type.flexibleTypeUpperBound?.let { flexible ->
            add("KOTLIN_FLEXIBILITY\t$declaration\t$position\t${flexible.typeFlexibilityId}")
            addType(declaration, "$position.flexibleUpperBound", flexible.type, typeParameters)
        }
    }

    private fun KmType.nullability(): String =
        when {
            isDefinitelyNonNull -> "DEFINITELY_NON_NULL"
            isNullable -> "NULLABLE"
            else -> "NON_NULL"
        }

    private fun KmClassifier.render(typeParameters: Map<Int, KmTypeParameter>): String =
        when (this) {
            is KmClassifier.Class -> "CLASS:${name.replace('/', '.')}"
            is KmClassifier.TypeAlias -> "TYPE_ALIAS:${name.replace('/', '.')}"
            is KmClassifier.TypeParameter ->
                "TYPE_PARAMETER:${typeParameters[id]?.name ?: error("Unknown type parameter id $id")}"
        }

    private fun Visibility.isApiVisibility(): Boolean =
        this == Visibility.PUBLIC || this == Visibility.PROTECTED

    private fun isEffectivelyPublicKotlinClass(type: Class<*>): Boolean {
        var current: Class<*>? = type
        while (current != null) {
            val annotation = current.getAnnotation(Metadata::class.java) ?: return false
            val metadata = KotlinClassMetadata.readStrict(annotation)
            if (
                metadata !is KotlinClassMetadata.Class ||
                    !metadata.kmClass.visibility.isApiVisibility()
            ) {
                return false
            }
            current = current.enclosingClass
        }
        return true
    }

    private fun fixtureClasses(packageSegment: String, vararg simpleNames: String): List<Class<*>> =
        simpleNames.map { simpleName ->
            Class.forName(
                "io.github.ulviar.procwright.kotlin.nullability.$packageSegment.$simpleName",
                false,
                KotlinSourceNullabilityCompatibility::class.java.classLoader,
            )
        }

    private fun normalizedFixtureSnapshot(type: Class<*>, packageSegment: String): Set<String> =
        normalizedFixtureSnapshot(listOf(type), packageSegment)

    private fun normalizedFixtureSnapshot(
        types: Collection<Class<*>>,
        packageSegment: String,
    ): Set<String> {
        val dottedPrefix = "io.github.ulviar.procwright.kotlin.nullability.$packageSegment"
        val binaryPrefix = "io/github/ulviar/procwright/kotlin/nullability/$packageSegment"
        return snapshot(types).mapTo(TreeSet()) { line ->
            line.replace(dottedPrefix, "fixture").replace(binaryPrefix, "fixture")
        }
    }

    private fun assertContains(snapshot: Set<String>, fragment: String) {
        check(snapshot.any { it.contains(fragment) }) {
            "Compiled metadata fixture did not exercise '$fragment': $snapshot"
        }
    }

    private fun isParameterNameSignature(signature: String): Boolean =
        signature.startsWith("KOTLIN_PARAMETER_NAME\t")

    private fun parameterNameKey(signature: String): String {
        val fields = signature.split('\t')
        return "${fields[1].substringBefore('(')}|${fields[2]}|${fields[3]}"
    }

    private fun kmType(classifier: KmClassifier): KmType =
        KmType().apply { this.classifier = classifier }

    private fun nullableType(classifier: KmClassifier): KmType =
        kmType(classifier).apply { isNullable = true }

    private fun valueParameter(name: String, type: KmType): KmValueParameter =
        KmValueParameter(name).apply { this.type = type }

    private fun className(root: Path, classFile: Path): String =
        root
            .relativize(classFile)
            .toString()
            .removeSuffix(".class")
            .replace(java.io.File.separatorChar, '.')

    private fun position(signature: String): String = signature.split('\t')[2]

    private fun writeBaseline(path: Path, snapshot: Set<String>) {
        Files.createDirectories(path.parent)
        Files.write(
            path,
            listOf("# Procwright public Kotlin source-nullability baseline", HEADER, "") +
                snapshot.sorted(),
            StandardCharsets.UTF_8,
        )
    }

    private fun verifyBaseline(path: Path, current: Set<String>) {
        check(Files.isRegularFile(path)) { "Kotlin source-nullability baseline is missing: $path" }
        val baseline =
            Files.readAllLines(path, StandardCharsets.UTF_8)
                .map(String::trim)
                .filter { it.isNotEmpty() && !it.startsWith('#') }
                .toSet()
        val diff = compatibilityDiff(baseline, current)
        check(diff.removedOrChanged.isEmpty() && diff.added.isEmpty()) {
            buildString {
                append("Public Kotlin source nullability no longer matches the baseline.")
                appendDiff("Removed or changed signatures", diff.removedOrChanged)
                appendDiff("Added signatures", diff.added)
            }
        }
    }

    private fun compatibilityDiff(baseline: Set<String>, current: Set<String>): CompatibilityDiff =
        CompatibilityDiff(TreeSet(baseline - current), TreeSet(current - baseline))

    private fun StringBuilder.appendDiff(title: String, signatures: Set<String>) {
        if (signatures.isNotEmpty()) {
            appendLine().append(title).append(':')
            signatures.take(80).forEach { appendLine().append("  ").append(it) }
        }
    }

    private data class CompatibilityDiff(val removedOrChanged: Set<String>, val added: Set<String>)
}

