    @JvmStatic
    fun main(args: Array<String>) {
        require(args.size == 2) {
            "Usage: KotlinSourceNullabilityCompatibility <check|write> <baseline>"
        }
        selfTest()
        val baseline = Path.of(args[1])
        val snapshot = publicApiSnapshot()
        when (args[0]) {
            "check" -> verifyBaseline(baseline, snapshot)
            "write" -> writeBaseline(baseline, snapshot)
            else -> error("Unknown mode: ${args[0]}")
        }
    }

    fun publicApiSnapshot(): Set<String> {
        val classesRoot =
            Path.of(
                ProtocolAdapterFactoryDsl::class.java.protectionDomain.codeSource.location.toURI()
            )
        val classes =
            Files.walk(classesRoot).use { paths ->
                paths
                    .filter(Files::isRegularFile)
                    .filter { it.fileName.toString().endsWith(".class") }
                    .filter { it.fileName.toString() != "module-info.class" }
                    .map { classFile -> className(classesRoot, classFile) }
                    .filter { it.startsWith("$API_PACKAGE.") }
                    .map {
                        Class.forName(it, false, ProtocolAdapterFactoryDsl::class.java.classLoader)
                    }
                    .filter { it.getAnnotation(Metadata::class.java) != null }
                    .toList()
            }
        return snapshot(classes)
    }

