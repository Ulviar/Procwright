    private fun verifyNullabilityMutation() {
        val approvedClass =
            Class.forName(
                "io.github.ulviar.procwright.kotlin.nullability.approved.ApiKt",
                false,
                KotlinSourceNullabilityCompatibility::class.java.classLoader,
            )
        val hostileClass =
            Class.forName(
                "io.github.ulviar.procwright.kotlin.nullability.hostile.ApiKt",
                false,
                KotlinSourceNullabilityCompatibility::class.java.classLoader,
            )
        val approved = normalizedFixtureSnapshot(approvedClass, "approved")
        val hostile = normalizedFixtureSnapshot(hostileClass, "hostile")
        val diff = compatibilityDiff(approved, hostile)
        val expectedChangedPositions =
            setOf(
                "typeParameter[T].bound[0]",
                "receiver",
                "receiver.typeArgument[0]",
                "receiver.typeArgument[1]",
                "receiver.typeArgument[1].typeArgument[0]",
                "parameter[0]",
                "parameter[0].typeArgument[0]",
                "parameter[0].typeArgument[1]",
                "parameter[0].typeArgument[1].typeArgument[0]",
                "return",
                "return.typeArgument[0]",
                "return.typeArgument[1]",
                "return.typeArgument[1].typeArgument[0]",
            )

        check(diff.removedOrChanged.map(::position).toSet() == expectedChangedPositions) {
            "Hostile fixture removed unexpected nullability positions: ${diff.removedOrChanged}"
        }
        check(diff.added.map(::position).toSet() == expectedChangedPositions) {
            "Hostile fixture added unexpected nullability positions: ${diff.added}"
        }
        check(diff.removedOrChanged.all { it.contains("\tNON_NULL\t") }) {
            "Approved fixture must be non-null at every changed position: ${diff.removedOrChanged}"
        }
        check(diff.added.all { it.contains("\tNULLABLE\t") }) {
            "Hostile fixture must weaken every changed position to nullable: ${diff.added}"
        }
    }

